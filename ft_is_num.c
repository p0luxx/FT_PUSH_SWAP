int	ft_is_num(char	*str)
{
	int	i;
	int	flag;

	i = 0;
	flag = 0;
	while (str[i])
	{
		if ((str[i] >= '0' && str[i] <= '9') || str[i] == 32)
			flag = 1;
		else
			flag = 0;
		i++;
	}
	return (flag);
}
